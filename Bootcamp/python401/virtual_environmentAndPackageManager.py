# sanal ortam list
# conda env list

# sanal ortam olusturma
# conda create -n [name]

# sanal ortam aktif etme
# conda activate [name]

# sanal ortam deaktif etme
# conda deactivate

# yuklu paketlerin listelenmesi
# conda list

# paket yukleme
# conda install [library_name]

# birden fazla paket yukleme
# conda install numpy pandas scipy

# paket kaldirma
# conda remove [library_name]

# belli bir versiona gore kurma
# conda install numpy=1.20.1

# paket guncelleme
# conda upgrade numpy

# tum paketleri guncelleme
# conda upgrade -all

# paket bilgilendirme enviroment.yaml
# conda env export > enviroment.yaml

# yaml yukleme
# conda env create -f enviroment.yaml

